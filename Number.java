public class Number {
	public static void main(String[] args){
		int num = 0b1010;//0b开头的是二进制 没有满32位前面全是0；转十进制10
		int num2 = 0b0101;//转十进制为5 long类型结尾加l float类型结尾加f
		System.out.println(num + "==="+num2);
		
	}
}
